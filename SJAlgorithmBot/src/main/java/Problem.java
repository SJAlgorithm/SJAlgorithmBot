public class Problem {
    int pNum;
    String pTitle;
    public Problem(int pNum, String pTitle) {
        this.pNum = pNum;
        this.pTitle = pTitle;
    }

    public int getpNum() {
        return pNum;
    }

    public void setpNum(int pNum) {
        this.pNum = pNum;
    }

    public String getpTitle() {
        return pTitle;
    }

    public void setpTitle(String pTitle) {
        this.pTitle = pTitle;
    }
}
